import UIKit

extension UIButton {
    
    func setSelectedColor() {
        self.backgroundColor = #colorLiteral(red: 0, green: 0.7768006921, blue: 0.2200525999, alpha: 1)
    }
    
    func setDeselectedColor() {
        self.backgroundColor = #colorLiteral(red: 0.6518810987, green: 0.8728772998, blue: 0.6689534783, alpha: 1)
    }
    
}
